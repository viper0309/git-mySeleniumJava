package com.java.tests;

import java.util.ArrayList;
import java.util.Iterator;

public class ArrayListDemo {

	public static void main(String[] args) {
		ArrayList arraylist=new ArrayList();
		arraylist.add("shreya");
		arraylist.add(300);
		arraylist.add(23.677);
		System.out.println("using for loop to print arrayList");
		int sizeOfArray=arraylist.size();
		for(int i=0;i<sizeOfArray;i++) {
			System.out.println(arraylist.get(i));
			System.out.println("using enhanced for loop to print arrayList");
		
			for (Object object : arraylist) {
				System.out.println(object);
				System.out.println("using iterator to print arrayList");
				Iterator value=arraylist.iterator();
				while(value.hasNext()) {
					Object valueOf=value.next();
					System.out.println(valueOf);
				}
			}
		}
		
		
		

	}
}


